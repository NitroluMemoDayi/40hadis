var index = 0;
var containerDiv = document.getElementById("container");
var hadisDiv = document.getElementById("hadis");
var turkce = document.querySelector(".turkce")
var idturkce = document.getElementById("turkce")
var sag = document.querySelector(".fa-chevron-right")
var sol = document.querySelector(".fa-chevron-left")

function showHadis() {
   
      var currentHadis = hadisler[index];
    hadisDiv.innerHTML = `
            <h3 class="turkce" id="turkce">${currentHadis.hadis} <br><br> <span class="kaynak">${currentHadis.kaynak}</span></h3>
    `;
    if(renkler[index].color == `largefont`){
      idturkce.classList.add("largefont")
    }
    containerDiv.style.backgroundImage = `url(${renkler[index].bg})`;
    
    sag.style.color = renkler[index].color;
    sol.style.color = renkler[index].color;
    hadisDiv.classList.remove("fade-out");
    hadisDiv.classList.remove("fade-in");
    void hadisDiv.offsetWidth;
    hadisDiv.classList.add("fade-in");
}

function hideHadis() {
    hadisDiv.classList.remove("fade-in");
    hadisDiv.classList.add("fade-out");
}

function nextHadis() {
    hideHadis();
    setTimeout(function() {
        if (index < hadisler.length - 1) {
            index++;
        } else {
            index = 0;
        }
        showHadis();
    }, 500);
}

function previousHadis() {
    hideHadis();
    setTimeout(function() {
        if (index > 0) {
            index--;
        } else {
            index = hadisler.length - 1;
        }
        showHadis();
    }, 500);
}

// İlk hadisi göstermek için
showHadis();


